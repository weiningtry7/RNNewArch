interface PostsData {
    posts: Post[]
    users: User[]
    topics: Topic[]
    postStatMap: any
    selfOperationMap: any
}

function recombinePostData(data: PostsData) {
    const { posts, users, topics } = data
    let postStatMap: any = {} //帖子状态:点赞数、评论数
    let selfOperationMap: any = {} //自己有没有操作过帖子:是否点赞过
    const userMap = users.reduce((acc: { [key: number]: User }, e: User) => {
        acc[e.uid] = e
        return acc
    }, {})
    //添加用户信息
    posts.forEach((post: Post) => {
        if (post.uid) {
            post.user = userMap[post.uid]
        }

        if (post.topicIds) {
            post.topics = post.topicIds.map(id => _.find(topics, (e) => e.id === id))
        }
        const { postId, postStat, selfOperation } = post
                //帖子状态
                if (postStat) {
                    postStatMap[postId] = postStat
                }
                //自己有没有操作过帖子
                if (selfOperation) {
                    selfOperationMap[postId] = selfOperation
                }
				//用作FlashList缓存池标识符
				post.cellIdentifier = cellIdentifierForPost(post)
    });
    data.postStatMap = postStatMap
    data.selfOperationMap = selfOperationMap
}


export function cellIdentifierForPost(post: Post): string {
  let identifier = 'Cell_Identifier_Text';
  if (post.cover) {
    identifier = 'Cell_Identifier_Cover';
  } else {
    //视频类型
    if (post.type === 3) {
      identifier = 'Cell_Identifier_Video';
    } else {
      //图片帖
      if (_.isEmpty(post.images)) {
      } else {
        const data = post.images;
        //单图分宽高图
        if (data.length == 1) {
          const image = data[0];
          if (image.width && image.height) {
            const originRatio = image.height / image.width
            if (originRatio >= 1.1) {
              identifier = 'Cell_Identifier_Image_Single_Tall';
            } else {
              identifier = 'Cell_Identifier_Image_Single_Wide';
            }
          } else {
            identifier = 'Cell_Identifier_Image_Single_Wide';
          }
        } else if (data.length == 2) {
          //两等分
          identifier = 'Cell_Identifier_Image_Double';
        } else {
          //三等分
          identifier = 'Cell_Identifier_Image_Triple';
        }
      }
    }
  }
  return identifier;
}
